// â”€â”€ Usage history helper (7 days, deterministic) â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
function makeDailyUsage(baseTokens: number, baseCost: number, baseRequests: number): DailyUsage[] {
  const dates = [
    "2026-02-12", "2026-02-13", "2026-02-14", "2026-02-15",
    "2026-02-16", "2026-02-17", "2026-02-18",
  ];
  const multipliers = [0.9, 1.1, 0.95, 1.05, 1.0, 0.85, 1.15];
  return dates.map((date, i) => ({
    date,
    tokens: Math.round(baseTokens * multipliers[i]),
    cost: Math.round(baseCost * multipliers[i] * 100) / 100,
    requests: Math.round(baseRequests * multipliers[i]),
  }));
}

// â”€â”€ Cost history helper (6 months, deterministic) â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
function makeCostHistory(
  baseAmount: number,
  vendorSplit: Record<"anthropic" | "openai" | "google", number>,
): MonthlyCost[] {
  const months = ["2025-09", "2025-10", "2025-11", "2025-12", "2026-01", "2026-02"];
  const trends = [0.85, 0.9, 0.95, 1.0, 1.05, 1.1];
  return months.map((month, i) => {
    const amount = Math.round(baseAmount * trends[i]);
    return {
      month,
      amount,
      byVendor: {
        anthropic: Math.round(amount * vendorSplit.anthropic),
        openai: Math.round(amount * vendorSplit.openai),
        google: Math.round(amount * vendorSplit.google),
      },
    };
  });
}

